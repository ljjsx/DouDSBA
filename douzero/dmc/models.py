class Model:
    """
    The wrapper for the three models. We also wrap several
    interfaces such as share_memory, eval, etc.
    """
    def __init__(self, device=0, version='base'):
        self.models = {}
        if not device == "cpu":
            device = 'cuda:' + str(device)
        if version == 'base':
            self.models['landlord'] = LandlordLstmModel().to(torch.device(device))
            self.models['landlord_up'] = FarmerLstmModel().to(torch.device(device))
            self.models['landlord_down'] = FarmerLstmModel().to(torch.device(device))
            print(f"model:base")
        elif version == 'res':
            self.models['landlord'] = LandlordLstmModel_res().to(torch.device(device))
            self.models['landlord_up'] = FarmerLstmModel_res().to(torch.device(device))
            self.models['landlord_down'] = FarmerLstmModel_res().to(torch.device(device))
            print(f"model:res")
        elif version == 'small':
            self.models['landlord'] = LandlordLstmModel_small().to(torch.device(device))
            self.models['landlord_up'] = FarmerLstmModel_small().to(torch.device(device))
            self.models['landlord_down'] = FarmerLstmModel_small().to(torch.device(device))
            print(f"model:small")
        elif version == 'big':
            self.models['landlord'] = LandlordLstmModel_big().to(torch.device(device))
            self.models['landlord_up'] = FarmerLstmModel_big().to(torch.device(device))
            self.models['landlord_down'] = FarmerLstmModel_big().to(torch.device(device))
            print(f"model:big")

    def forward(self, position, z, x, training=False, flags=None):
        model = self.models[position]
        return model.forward(z, x, training, flags)

    def share_memory(self):
        self.models['landlord'].share_memory()
        self.models['landlord_up'].share_memory()
        self.models['landlord_down'].share_memory()

    def eval(self):
        self.models['landlord'].eval()
        self.models['landlord_up'].eval()
        self.models['landlord_down'].eval()

    def parameters(self, position):
        return self.models[position].parameters()

    def get_model(self, position):
        return self.models[position]

    def get_models(self):
        return self.models
